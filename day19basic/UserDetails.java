package day19basic;

public class UserDetails {
	String firstName;
	String lastName;
	String emailId;
	String mobileNumber;
	String password;

	public UserDetails(String firstName, String lastName, String emailId, String mobileNumber, String password) {
		super();
		this.firstName = firstName;
		this.lastName = lastName;
		this.emailId = emailId;
		this.mobileNumber = mobileNumber;
		this.password = password;
	}

}
